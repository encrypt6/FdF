#include "../libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
		i++;
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
